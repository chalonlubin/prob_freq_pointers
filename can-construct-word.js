function freqCount(input) {
  // freqCount: build a seperate function labeled freqCount
  // Input: string2
  // Output: Object

  let counter = {};

  for (let ele of input) {
    counter[ele] = counter[ele] ? counter[ele] + 1 : 1;
  }
  // - populate object with a key for each unique value in input
  // - count the frequency of each key in input, assign it the value of the key

  // return ouput
  return counter;
}

function canConstructWord(word, letters) {
  // generate an object from freqCount

  const lettersCount = freqCount(letters);
  // compare values in string1 to object generated by freqCount on string2.
  // if key[value] is falsy return false;
  // else decrement key[value] (no re-use).
  // otherwise return true.

  for (let char of word) {
    if (lettersCount[char]) {
      lettersCount[char]--;
    } else {
      return console.log(false);
    }

  }
  return console.log(true);
}

